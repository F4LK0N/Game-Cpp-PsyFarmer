#include "Modal.h"



//#####################################################################################################################################################################
Modal::Modal()
{
	message.setAncora(0.5, 0.5);
	message.setString("");
	buttonText.setString("Pressione ENTER");
	buttonText.setAncora(1, 1);
}
//#####################################################################################################################################################################
//#####################################################################################################################################################################
//#####################################################################################################################################################################



//#####################################################################################################################################################################
Modal::~Modal(){}
//#####################################################################################################################################################################
//#####################################################################################################################################################################
//#####################################################################################################################################################################



//#####################################################################################################################################################################
void Modal::setMsg(string message){
	this->message.setString(message);
	this->message.setFonte("fMenu");
	this->buttonText.setFonte("fMenuMini");
	this->messageBKP=message;
}
//#####################################################################################################################################################################
//#####################################################################################################################################################################
//#####################################################################################################################################################################



//#####################################################################################################################################################################
string Modal::getMsg(){
	return messageBKP; }
//#####################################################################################################################################################################
//#####################################################################################################################################################################
//#####################################################################################################################################################################



//#####################################################################################################################################################################
bool Modal::run(){
	message.desenhar(gJanela.getLargura()/2, gJanela.getAltura()/2);
	buttonText.desenhar((gJanela.getLargura()/12)*11, (gJanela.getAltura()/12)*11);

	//CLOSE
	if(gTeclado.pressionou[TECLA_ENTER] || gTeclado.pressionou[TECLA_ESC] || gTeclado.pressionou[TECLA_E] || gTeclado.pressionou[TECLA_D]){
		return false; }

	//LET OPEN
	return true;
}
